async function connectWallet() {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        try {
            // Request account access
            await window.ethereum.request({ method: 'eth_requestAccounts' });
            const accounts = await web3.eth.getAccounts();
            userAccount = accounts[0];
            console.log("Connected account:", userAccount);
            document.getElementById('wallet-address').innerText = userAccount;

            // Show deposit and withdraw sections
            document.getElementById('deposit-section').style.display = 'block';
            document.getElementById('withdraw-section').style.display = 'block';

            // Initialize the contract instances
            vaultContract = new web3.eth.Contract(vaultABI, vaultAddress);
            usdcContract = new web3.eth.Contract(usdcABI, usdcAddress);
        } catch (error) {
            console.error("User denied account access", error);
        }
    } else {
        alert("Non-Ethereum browser detected. You should consider trying MetaMask!");
    }
}

async function depositUSDC() {
    const amount = document.getElementById('depositAmount').value;
    if (amount > 0) {
        const amountInWei = web3.utils.toWei(amount, "mwei"); // USDC uses 6 decimals, so use 'mwei' instead of 'ether'
        try {
            // Approve the Vault contract to spend USDC on the user's behalf
            await usdcContract.methods.approve(vaultAddress, amountInWei).send({ from: userAccount });

            // Deposit the approved amount to the Vault contract
            await vaultContract.methods.deposit(amountInWei).send({ from: userAccount });
            document.getElementById('deposit-status').innerText = "Deposit successful!";
        } catch (error) {
            console.error("Deposit failed", error);
            document.getElementById('deposit-status').innerText = "Deposit failed. Check console for details.";
        }
    } else {
        alert("Please enter a valid amount to deposit.");
    }
}

async function withdrawUSDC() {
    const amount = document.getElementById('withdrawAmount').value;
    if (amount > 0) {
        const amountInWei = web3.utils.toWei(amount, "mwei"); // Again, USDC uses 6 decimals
        try {
            await vaultContract.methods.withdraw(amountInWei).send({ from: userAccount });
            document.getElementById('withdraw-status').innerText = "Withdrawal successful!";
        } catch (error) {
            console.error("Withdrawal failed", error);
            document.getElementById('withdraw-status').innerText = "Withdrawal failed. Check console for details.";
        }
    } else {
        alert("Please enter a valid amount to withdraw.");
    }
}

